public enum BladeEnum
{
    MINI_BLADE, // increases next attack damage by 10%
    REGULAR_BLADE, // increases next attack damage by 30%
    ENCHANTED_BLADE, // increases next attack damage by 50%
    PIERCE_BLADE, // increases next attack pierce by 20%
    PIPWISE_BLADE, // exhausts all pips, increasing next attack damage by 10% per pip
    PIPWISE_PIERCE_BLADE, // exhausts all pips, increasing next attack pierce by 10% per pip
}